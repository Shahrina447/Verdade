def fill_template(template: str, fills: dict) -> str:
    import re
    result = template
    for key in re.findall(r'\{(\w+)\}', template):
        if key in fills:
            result = result.replace("{" + key + "}", random.choice(fills[key]), 1)
    return result


def generate_samples(n: int, templates: list, fills: dict, label: int) -> list[dict]:
    samples = []
    used = set()
    attempts = 0
    while len(samples) < n and attempts < n * 100:
        text = fill_template(random.choice(templates), fills)
        attempts += 1
        if text not in used:
            used.add(text)
            samples.append({"News Items": text, "label": label})
    return samples


